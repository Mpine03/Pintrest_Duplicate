const mainContent = document.getElementById('main-content');
const searchBar = document.getElementById('search-bar');

function displayItems(items) {
    mainContent.innerHTML = '';
    items.forEach(item => {
        const itemElement = document.createElement('div');
        itemElement.classList.add('item');
        itemElement.innerHTML = `
            <div class="image-container">
                <img src="${item.imageUrl}" alt="${item.title}">
                <div class="hover-content">
                    <div class="category">
                        ${item.suggestedCategory}
                        <i class="fas fa-chevron-down "></i>
                    </div>
                    <div class="save-button">Save</div>
                    <div class="bottom-right-icons">
                        <i class="fas fa-download icon-button"></i>
                        <i class="fas fa-ellipsis-h icon-button"></i>
                    </div>
                </div>
            </div>
            <div class="title">${item.title}</div>
            <div class="profile">
                <img src="${item.imageProfileImage}" alt="${item.profileName}">
                <span class="profile-name">${item.profileName}</span>
            </div>
        `;
        mainContent.appendChild(itemElement);
    });

    
    masonryLayout();
}

function filterItems(event) {
    const query = event.target.value.toLowerCase();
    const filteredItems = items.filter(item => 
        item.title.toLowerCase().includes(query) || 
        item.profileName.toLowerCase().includes(query)
    );
    displayItems(filteredItems);
}


displayItems(items);


searchBar.addEventListener('input', filterItems);


function masonryLayout() {
    const columns = 5; 
    const containerWidth = mainContent.clientWidth;
    const gutter = 20;
    const itemWidth = (containerWidth / columns) - gutter;
    const itemElements = mainContent.querySelectorAll('.item');
    let columnHeights = Array.from({ length: columns }, () => 0);

    itemElements.forEach(item => {
        const minHeight = Math.min(...columnHeights);
        const columnIndex = columnHeights.indexOf(minHeight);
        const leftOffset = columnIndex * (itemWidth + gutter);
        const topOffset = columnHeights[columnIndex];

        item.style.position = 'absolute';
        item.style.width = `${itemWidth}px`; 
        item.style.top = `${topOffset}px`;
        item.style.left = `${leftOffset}px`;

        columnHeights[columnIndex] += item.offsetHeight + gutter; 
        mainContent.style.height = `${Math.max(...columnHeights)}px`; 
    });
}


window.addEventListener('load', () => {
    masonryLayout();
});

window.addEventListener('resize', masonryLayout);